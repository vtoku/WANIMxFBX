import { chromium } from "playwright";
import { readFileSync } from "node:fs";
const URL = "http://localhost:5173/WANIMxFBX/";
const WANIM = process.argv[2] ?? "C:\\Users\\VTOKU\\Downloads\\takyon take 2.wanim";
const browser = await chromium.launch();
const page = await browser.newPage({ viewport: { width: 900, height: 820 } });
const errors = [];
page.on("console", (m) => { if (m.type() === "error") errors.push(m.text()); });
await page.goto(URL, { waitUntil: "networkidle" });
await page.setInputFiles("#file-input", { name: "c.wanim", mimeType: "application/octet-stream", buffer: readFileSync(WANIM) });
await page.waitForSelector("#loaded-state:not([hidden])", { timeout: 20000 });
await page.waitForTimeout(3500);
const cv = await page.$("#viewport canvas"); const b = await cv.boundingBox();
await page.mouse.move(b.x + b.width / 2, b.y + b.height / 2);
for (let i = 0; i < 10; i++) await page.mouse.wheel(0, 240);
await page.waitForTimeout(500);
await page.screenshot({ path: "scripts/bundled-check.png" });
if (errors.length) errors.slice(0, 5).forEach((e) => console.log("ERR", e)); else console.log("no errors");
await browser.close();
